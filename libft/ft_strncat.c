#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int		i;
	size_t	k;

	k = 0;
	i = ft_strlen(s1);
	while ((k < n) && (s2[k] != '\0'))
	{
		s1[i] = s2[k];
		i++;
		k++;
	}
	s1[i] = '\0';
	return (s1);
}
